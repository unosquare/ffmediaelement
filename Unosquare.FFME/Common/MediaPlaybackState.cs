namespace Unosquare.FFME.Common
{
    /// <summary>
    /// Media States compatible with MediaState enumeration.
    /// </summary>
    public enum MediaPlaybackState
    {
        /// <summary>
        /// The manual status
        /// </summary>
        Manual = 0,

        /// <summary>
        /// The play status
        /// </summary>
        Play = 1,

        /// <summary>
        /// The close status
        /// </summary>
        Close = 2,

        /// <summary>
        /// The pause status
        /// </summary>
        Pause = 3,

        /// <summary>
        /// The stop status
        /// </summary>
        Stop = 4
    }
}
