// ReSharper disable UnusedMember.Global
namespace Unosquare.FFME.ClosedCaptions
{
    /// <summary>
    /// Enumerates the different Closed-Captioning Colors
    /// </summary>
    public enum CaptionsColor
    {
        /// <summary>
        /// No color
        /// </summary>
        None = 0x00,

        /// <summary>
        /// The white color
        /// </summary>
        White = 0x20,

        /// <summary>
        /// The white transparent color
        /// </summary>
        WhiteTransparent = 0x21,

        /// <summary>
        /// The green color
        /// </summary>
        Green = 0x22,

        /// <summary>
        /// The green transparent color
        /// </summary>
        GreenTransparent = 0x23,

        /// <summary>
        /// The blue color
        /// </summary>
        Blue = 0x24,

        /// <summary>
        /// The blue transparent color
        /// </summary>
        BlueTransparent = 0x25,

        /// <summary>
        /// The cyan color
        /// </summary>
        Cyan = 0x26,

        /// <summary>
        /// The cyan transparent color
        /// </summary>
        CyanTransparent = 0x27,

        /// <summary>
        /// The red color
        /// </summary>
        Red = 0x28,

        /// <summary>
        /// The red transparent color
        /// </summary>
        RedTransparent = 0x29,

        /// <summary>
        /// The yellow color
        /// </summary>
        Yellow = 0x2A,

        /// <summary>
        /// The yellow transparent color
        /// </summary>
        YellowTransparent = 0x2B,

        /// <summary>
        /// The magenta color
        /// </summary>
        Magenta = 0x2C,

        /// <summary>
        /// The magenta transparent color
        /// </summary>
        MagentaTransparent = 0x2D,

        /// <summary>
        /// The white italics color
        /// </summary>
        WhiteItalics = 0x2E,

        /// <summary>
        /// The white italics transparent color
        /// </summary>
        WhiteItalicsTransparent = 0x2F,

        /// <summary>
        /// The background transparent color
        /// </summary>
        BackgroundTransparent = 0x2D00,

        /// <summary>
        /// The foreground black color
        /// </summary>
        ForegroundBlack = 0x2E00,

        /// <summary>
        /// The foreground black underline color
        /// </summary>
        ForegroundBlackUnderline = 0x2F00
    }
}
